# operasi yang dilakukan dengan penyingkatan

a = 5 # assigment
print('nilai a = ',a)

a += 1 #artinya adalah a = a + 1
print('nilai a = a + 1, nilai a adalah',a)

a -= 2 # a = a - 2
print('nilai a = a -= 2, nilai a adalah',a)

a *= 5 # a = a * 2
print('nilai a = a *= 2, nilai a adalah',a)

a /= 2 # a = a / 2
print('nilai a = a /= 2, nilai a adalah',a)

