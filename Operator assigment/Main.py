# operasi bitwise\
# OR
c = True
c |= False
print('\nnilai c |= False, nilai c menjadi ',c)

c = False
c |= False
print('\nnilai c |= False, nilai c menjadi ',c)

# AND
c = True
c &= False
print('\nnilai c &= False, nilai c menjadi ',c)

c = True
c &= True
print('\nnilai c &= False, nilai c menjadi ',c)

# XOR
c = True
c ^= False
print('\nnilai c ^= False, nilai c menjadi ',c)

c = False
c ^= False
print('\nnilai c ^= False, nilai c menjadi ',c)