# Operator Bitwise,operasi biner,binary

a = 9 
b = 5

# biteise or (|)
c = a|b
print('\n====OR====')
print('nilai :',a,', binary :',format(a,'08b'))
print('nilai :',b,', binary :',format(b,'08b'))
print(7*'=','|',7*'=')
print('nilai :',c,', binary :',format(c,'08b'))

# operator bitwise and (&)
c = a&b
print('\n====AND====')
print('nilai :',a,', binary :',format(a,'08b'))
print('nilai :',b,', binary :',format(b,'08b'))
print(7*'=','&',7*'=')
print('nilai :',c,', binary :',format(c,'08b'))

#bitwise XOR
c = a^b
print('\n====XOR====')
print('nilai :',a,', binary :',format(a,'08b'))
print('nilai :',b,', binary :',format(b,'08b'))
print(7*'=','^',7*'=')
print('nilai :',c,', binary :',format(c,'08b'))