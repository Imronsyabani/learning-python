a = 10
b = 4

#Pertambahan
hasil = a + b
print(a,'+',b,'=',hasil)
#Pengurangan
hasil = a - b
print(a,'-',b,'=',hasil)
#Perkalian
hasil = a * b
print(a,'X',b,'=',hasil)
#Pembagian
hasil = a / b
print(a,'/',b,'=',hasil)
#Eksponen(Perpangkatan)
hasil = a ** b
print(a,'(',b,')','=',hasil)
#Modulus
hasil = a % b
print(a,'%',b,'=',hasil)
#Angka Bulat
hasil = a // b
print(a,'//',b,'=',hasil)

